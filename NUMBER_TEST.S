TEST_ISNUMBER:
	NC W_0X0; NC W_0X0; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	NC W_0X5; NC W_0X1; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	j 10f
1:
	.ASCII "$0"
2:
	.ASCII "0123"
3:
	.ASCII "$0123456789ABCDEF"
4:
	.ASCII "$/"
5:
	.ASCII "$G"
6:
	.ASCII "$:"
7:
	.ASCII "$@"
8:
	.ASCII "$ABCDEFG"
9:
	.P2ALIGN 1, 0
10:
	LITA 1b; LIT 2; NC W_ISNUMBER; NC W_TRUE; NC W_EQCHK;
	LITA 2b; LIT 4; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	LITA 3b; LIT 9; NC W_ISNUMBER; NC W_TRUE; NC W_EQCHK;
	LITA 4b; LIT 2; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	LITA 5b; LIT 2; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	LITA 6b; LIT 2; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	LITA 7b; LIT 2; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;
	LITA 8b; LIT 8; NC W_ISNUMBER; NC W_FALSE; NC W_EQCHK;

	NC W_PZCHK

TEST_TONUMBER:
	NC W_0X0; NC W_0X0; NC W_TONUMBER; NC W_0X0; NC W_EQCHK;
	j 10f
1:
	.ASCII "$"
2:
	.ASCII "$5"
3:
	.ASCII "A"
4:
	.ASCII "$01234567"
5:
	.ASCII "89ABCDEF"
9:
	.P2ALIGN 1, 0
10:
	LITA 1b; LIT 1; NC W_TONUMBER; NC W_0X0; NC W_EQCHK;
	LITA 2b; LIT 2; NC W_TONUMBER; NC W_0X5; NC W_EQCHK;
	LITA 3b; LIT 1; NC W_TONUMBER; NC W_0XA; NC W_EQCHK;
	LITA 4b; LIT 9; NC W_TONUMBER; LIT 0x01234567; NC W_EQCHK;
	LITA 5b; LIT 8; NC W_TONUMBER; LIT 0x89ABCDEF; NC W_EQCHK;

	NC W_PZCHK
